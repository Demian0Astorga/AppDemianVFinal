package com.example.appdemianvfinal.pyme;

public class Pyme {

    String NombrePyme;
    String TipoTienda;
    String CorreoE;
    String Contrasena;

    public String getNombrePyme() {
        return NombrePyme;
    }

    public String getTipoTienda() {
        return TipoTienda;
    }

    public String getCorreoE() {
        return CorreoE;
    }

    public String getContrasena() {
        return Contrasena;
    }

    public void setNombrePyme(String nombrePyme) {
        NombrePyme = nombrePyme;
    }

    public void setTipoTienda(String tipoTienda) {
        TipoTienda = tipoTienda;
    }

    public void setCorreoE(String correoE) {
        CorreoE = correoE;
    }

    public void setContrasena(String contrasena) {
        Contrasena = contrasena;
    }

}
